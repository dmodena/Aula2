package com.dmodena.model;

/**
 *
 * @author dmodena
 */
public class Calculadora {
    public static int adicionar(int parcela1, int parcela2) {
        return parcela1 + parcela2;
    }
    
    public static float adicionar(float parcela1, float parcela2) {
        return parcela1 + parcela2;
    }
    
    public static double adicionar(double parcela1, double parcela2) {
        return parcela1 + parcela2;        
    }
    
    public static int subtrair(int minuendo, int subtraendo) {
        return minuendo - subtraendo;
    }
    
    public static float subtrair(float minuendo, float subtraendo) {
        return minuendo - subtraendo;
    }
    
    public static double subtrair(double minuendo, double subtraendo) {
        return minuendo - subtraendo;
    }
    
    public static int multiplicar(int fator1, int fator2) {
        return fator1 * fator2;
    }
    
    public static float multiplicar(float fator1, float fator2) {
        return fator1 * fator2;
    }
    
    public static double multiplicar(double fator1, double fator2) {
        return fator1 * fator2;
    }
    
    public static int dividir(int dividendo, int divisor) {
        return dividendo / divisor;
    }
    
    public static float dividir(float dividendo, float divisor) {
        return dividendo / divisor;
    }
    
    public static double dividir(double dividendo, double divisor) {
        return dividendo / divisor;
    }
}
